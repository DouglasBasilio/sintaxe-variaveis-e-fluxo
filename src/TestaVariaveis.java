
public class TestaVariaveis {
	public static void main(String[] args) {
		System.out.println("Olá novo teste");
		
		int idade;
		idade = 30;
		System.out.println(idade);
		
		idade = 30 + 10;
		System.out.println(idade);
		
		idade = (10 * 3) + 1;
		System.out.println(idade);	
		
		System.out.println("A idade é " + idade + " parabéns!");
		
	}
}
